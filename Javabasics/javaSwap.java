package Javabasics;

import java.util.*;

public class javaSwap {

    public static void main(String[] args) {

        int a = 10;
        int b = 20;

        // SWAP
        int temp = b;
        b = a;
        a = temp;

        System.out.println("a= " + a);
        System.out.println("b= " + b);
    }

}
